let animals_name = ["Hourse", "Lion", "Camel"]

for (animals of animals_name){
    if(animals === "Hourse"){
        console.log(`${animals} is a Faster Animal.`)
    }
    else if(animals === "Lion"){
        console.log(`${animals} is a King Of all Animals.`);
    }
    else if (animals === "Camel"){
        console.log(`${animals} is a Tall Animals.`);
    }
}
